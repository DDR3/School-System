    public void connectDB() {
        try { // Automatically connected to database after the subject info was click 
            Class.forName(sqlDriver);
            connect = DriverManager.getConnection(url, sqlUser, sqlPass);
            statement = connect.createStatement();
            String querySubject = "SELECT * FROM subjects";
            result = statement.executeQuery(querySubject);
            table = (DefaultTableModel)subjectTable.getModel();
            if (connect != null) {
                System.out.println("Database Connected");
            }     
            while(result.next()) {
                table.addRow(new String[]{result.getString("subject_id"), result.getString("subject_name")});
            }        
        } catch (SQLException ex) { // Exception for SQL
            Logger.getLogger(Subject.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Database Offline!", "Warning", JOptionPane.WARNING_MESSAGE);
        } catch (ClassNotFoundException ex) { //Exception for Class.forName()
            Logger.getLogger(Subject.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
