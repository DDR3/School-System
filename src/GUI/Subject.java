    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    try {
        // Check if the subject_id already exists in the database
        String checkQuery = "SELECT * FROM subjects WHERE subject_id = ?";
        prestate = connect.prepareStatement(checkQuery);
        prestate.setString(1, subjectID.getText());
        result = prestate.executeQuery();

        if (result.next()) {
            // subject_id already exists, perform the update
            String editSubject = "UPDATE subjects SET subject_name=? WHERE subject_id=?";
            prestate = connect.prepareStatement(editSubject);
            prestate.setString(1, subjectName.getText());
            prestate.setString(2, subjectID.getText());
            prestate.executeUpdate();
            // showMessageDialog
             JOptionPane.showMessageDialog(this, "Edit Successfully!");
            // Clear all input
            subjectID.setText("");
            subjectName.setText("");
            // disable save, add & delete button
            btnSave.setEnabled(false);
            btnAdd.setEnabled(false);
            btnDelete.setEnabled(false);
            // automatically update the table
            table.setRowCount(0);
            connectDB();
        } 
    } catch (SQLException ex) {
         // Exception for SQL
         Logger.getLogger(Subject.class.getName()).log(Level.SEVERE, null, ex);
         JOptionPane.showMessageDialog(null, "Database Offline!", "Warning", JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_btnSaveActionPerformed

