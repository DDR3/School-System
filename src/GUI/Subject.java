    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try { // check if subject ID already exists
            String checkQuery = "SELECT * FROM subjects WHERE subject_id = ?";
            prestate = connect.prepareStatement(checkQuery);
            prestate.setString(1, subjectID.getText());
            result = prestate.executeQuery();
            if(result.next()) {
                JOptionPane.showMessageDialog(this, "Subject ID already exists!", "Warning", JOptionPane.WARNING_MESSAGE);
            } // add to databebase if subjectID is a number  
            else {
            String addSubject = "INSERT into subjects (subject_id, subject_name) VALUES (?, ?)";
            prestate = connect.prepareStatement(addSubject);
            prestate.setString(1, subjectID.getText());
            prestate.setString(2, subjectName.getText());
            prestate.executeUpdate();         
            // showMessageDialog
            JOptionPane.showMessageDialog(this, "Added Successfully!");
            //Clear all input
            subjectID.setText("");
            subjectName.setText("");
            // disable add button
            btnAdd.setEnabled(false);
            // automatically update the table
            table.setRowCount(0);
            connectDB();                  
            }
        } catch (SQLException ex) { // Exception for SQL
            Logger.getLogger(Subject.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Database Offline!", "Warning", JOptionPane.WARNING_MESSAGE);
        } 
    }//GEN-LAST:event_btnAddActionPerformed

