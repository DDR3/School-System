    private void searchBoxKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBoxKeyReleased
        try {
            String querySearch = "SELECT student_id, first_name, last_name, section FROM student_info WHERE student_id=? OR first_name=? OR last_name=? OR section=? ";
            prestate = connect.prepareStatement(querySearch);
            prestate.setString(1, searchBox.getText());
            prestate.setString(2, searchBox.getText());
            prestate.setString(3, searchBox.getText());   
            prestate.setString(4, searchBox.getText());
            result = prestate.executeQuery();
            table.setRowCount(0);
            while(result.next()){
                table.addRow(new String[]{result.getString("student_id"), result.getString("first_name"), result.getString("last_name"), result.getString("section")});
            }   
            // automatically get table data
            if(searchBox.getText().isEmpty()) {
                connectDB();
            }            
        } catch (SQLException ex) {
            Logger.getLogger(SearchStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchBoxKeyReleased

